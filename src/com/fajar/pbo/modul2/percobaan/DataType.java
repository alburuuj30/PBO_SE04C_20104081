package com.fajar.pbo.modul2.percobaan;

public class DataType {
    public static void main(String[] args) {
        // tipe dta primitif

        long data1 = 767226531;
        int data2 = 2235641;
        short data3 = 714;
        byte data4 = 34;

        float data6 = (float) 1.733;
        double data5 = 4.967;

        char data7 = 'C';
        boolean data8 = true;


        System.out.println("Nilai Long  \t: " + data1);
        System.out.println("Nilai Int  \t\t: " + data2);
        System.out.println("Nilai Short  \t: " + data3);
        System.out.println("Nilai Byte  \t: " + data4);
        System.out.println("Nilai Double  \t: " + data5);
        System.out.println("Nilai Float  \t: " + data6);
        System.out.println("Nilai Char  \t: " + data7);
        System.out.println("Nilai Boolean  \t: " + data8);


    }
}
